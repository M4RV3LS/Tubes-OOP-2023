public class Dimension {
    private int width;
    private int length;
    //constructor
    public Dimension(int width, int length) {
        this.width = width;
        this.length = length;
    }
    //getter setter
    public int getWidth() {
        return width;
    }
    public void setWidth(int width) {
        this.width = width;
    }
    public int getLength() {
        return length;
    }
    public void setLength(int length) {
        this.length = length;
    }
}
